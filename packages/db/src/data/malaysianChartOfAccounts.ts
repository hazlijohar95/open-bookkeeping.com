// Helper to get account by code
export function getAccountByCode(code: string): DefaultAccount | undefined {
  return malaysianSMEAccounts.find((a) => a.code === code);
}

// Helper to get child accounts
export function getChildAccounts(parentCode: string): DefaultAccount[] {
  return malaysianSMEAccounts.filter((a) => a.parentCode === parentCode);
}

// Helper to get root accounts (no parent)
export function getRootAccounts(): DefaultAccount[] {
  return malaysianSMEAccounts.filter((a) => !a.parentCode);
}

// Helper to get accounts by type
export function getAccountsByType(type: AccountType): DefaultAccount[] {
  return malaysianSMEAccounts.filter((a) => a.accountType === type);
}

// Helper to get system accounts
export function getSystemAccounts(): DefaultAccount[] {
  return malaysianSMEAccounts.filter((a) => a.isSystemAccount);
}
